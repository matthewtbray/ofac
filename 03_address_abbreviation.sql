IF OBJECT_ID(N'dbo.Address_Abbreviation', N'U') IS NULL
BEGIN
    CREATE TABLE dbo.Address_Abbreviation (
        ID                         INT          NOT NULL IDENTITY(1,1) PRIMARY KEY,
        Address_Part_Abbreviation  VARCHAR(20)  NOT NULL,
        Address_Part               VARCHAR(100) NOT NULL,
        Skip_At_Beginning          BIT          NOT NULL DEFAULT 0
    );
    CREATE INDEX IX_AddrAbbrev ON dbo.Address_Abbreviation (Address_Part_Abbreviation);
END;
