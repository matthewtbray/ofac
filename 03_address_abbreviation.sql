-- 03_address_abbreviation.sql
-- Creates and populates dbo.Address_Abbreviation.
--
-- Columns
--   Address_Part_Abbreviation  the token as it appears in raw address data
--   Address_Part               the canonical expanded form
--   Skip_At_Beginning          1 = this mapping should be skipped when the
